				<h1>Sewage Leaks in Vermont</h1>
				<p>Sewage leaks can be a serious problem in Vermont.   Sanitation workers in Vermont try their hardest to keep up with the heavy rains and melting snows in the spring.  However, due to torrential rains, and severely outdated equipment leaks remain a serious problem.  Fortunately this is not untreated sewage escaping, but water from the storm drains.  This storm drain water contains toxins and runoff from the roads such as motor oil.  When the leaks occur these toxins flow downstream polluting Vermont’s lakes.  Left unchecked, the dilapidated sewage system can cause significant harm to Vermont’s ecosystem, such as furthering the spread of algae blooms.</p> <br>
				<p>Many of the plants in the state were built in the 1970’s and have received no major renovations since then.  This problem will only be solved with major upgrade to Vermont wastewater facilities.  Voters need to pressure their representatives in order to find a solution to this problem.  Which will only be possible with increased voter awareness.  Articles such as the one by VPR or this one, as well as alerts of sewage leaks help to spread awareness so this problem can be solved.  For a state as dependent on it’s lakes and waterways, Vermont should have water pollution as a top priority.</p> <br>
